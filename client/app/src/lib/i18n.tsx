// ─── Type & Context ───────────────────────────────────────────────────────────
export type TranslationKey = keyof typeof en;

interface LangContext {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: TranslationKey) => string;
}

const LangContext = createContext<LangContext>({
  lang: "am",
  setLang: () => {},
  t: (key) => key,
});

const translations: Record<Lang, typeof en> = { en, ru, am };
const LANG_STORAGE_KEY = "viva_lang";

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => {
    if (typeof window === "undefined") return "am";
    const saved = window.localStorage.getItem(LANG_STORAGE_KEY);
    return saved === "en" || saved === "ru" || saved === "am" ? saved : "am";
  });
  useEffect(() => {
    if (typeof window !== "undefined") {
      window.localStorage.setItem(LANG_STORAGE_KEY, lang);
      document.cookie = `viva_lang=${lang}; path=/; max-age=31536000; samesite=lax`;
    }
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang;
    }
  }, [lang]);
  const t = (key: TranslationKey): string => {
    if (lang === "am") {
      return translations.am[key] ?? key;
    }
    return translations[lang][key] ?? translations.en[key] ?? key;
  };
  return (
    <LangContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LangContext.Provider>
  );
}

export function useLang() {
  return useContext(LangContext);
}
